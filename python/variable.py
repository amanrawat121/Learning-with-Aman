# you can create variables in python like below
a=1
b=2
c=3
print(a)
print(b)
print(c)

# you can use casting to assign datatype like below
a=int(1)
b=str(2)
c=float(3)

# you can get the datatype with type() function like:
print(type(a))
print(type(b))
print(type(c))

# you can use single or double quotes to show strings like below

a="hello"
a='hello'
print(a)

print("variable names are always case sensitive")